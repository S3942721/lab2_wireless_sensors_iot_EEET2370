function [symbols_out] = func3(symbols_in,ppm,rdd)
%   This code has been written by Bassel Al Homssi based on the LoRa
%   docding the PHY layer based on rpp0.
%   This code is to demodulate the LoRa signal to FSK signal
%   Code last updated 17.07.2019
symbols_out = [] ;
sym_idx_ext = 1 ;
for block_idx = 1 : floor(length(symbols_in)/(4+rdd))
    sym_int = zeros(1,ppm) ;
    for sym_idx = 1 : 4 + rdd
        sym_rot = rotl(symbols_in(sym_idx_ext),sym_idx-1,ppm) ;
        mask = bitsll(1,ppm-1) ;
        ctr = ppm ;
        while mask > 0
            sym_int(ctr) = sym_int(ctr) + bitsll(double(bitand(sym_rot,mask)>0),sym_idx-1) ;
            mask = floor(bitsra(mask,1)) ;
            ctr = ctr - 1 ;
        end
        sym_idx_ext = sym_idx_ext + 1 ;
    end
    symbols_out = [symbols_out sym_int] ;
end
end
function [symbols_out] = func4(symbols_in,N)
%   This code has been written by Bassel Al Homssi based on the LoRa
%   docding the PHY layer based on rpp0.
%   This code is to demodulate the LoRa signal to FSK signal
%   Code last updated 17.07.2019
pattern = [5 0 1 2 4 3 6 7] ;
symbols_out = zeros(1,N) ;
for ctr = 1 : N
    for Ctr = 1 : length(pattern)
        symbols_out(ctr) = symbols_out(ctr) + bitsll(double(bitand(symbols_in(ctr),bitsll(1,pattern(Ctr)))>0),Ctr-1) ;
    end
end
end
