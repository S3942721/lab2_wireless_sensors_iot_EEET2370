function [y] = rotl(bits,count,size)
%   This code has been written by Bassel Al Homssi based on the LoRa
%   docding the PHY layer based on rpp0.
%   This code is to demodulate the LoRa signal to FSK signal
%   Code last updated 17.07.2019
len_mask = bitsll(1,size) - 1 ;
count = mod(count,size) ;
bits = bitand(bits,len_mask) ;
y = bitor(bitand(bitsll(bits,count),len_mask), floor(bitsra(bits,size - count))) ;
end
function [r] = slb(data,indices)
