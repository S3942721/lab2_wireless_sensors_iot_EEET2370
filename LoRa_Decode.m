%   docding the PHY layer based on rpp0.
%   This code is to demodulate the LoRa signal to FSK signal
%   Code last updated 17.07.2019
r = 0 ;
for ctr = 0 : length(indices) - 1
    if bitand(data,bitsll(1,indices(ctr+1))) > 0
        r = r + bitsll(1,ctr) ;
    else
        r = r + 0 ;
    end
end
