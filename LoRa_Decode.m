function [symbols_out] = LoRa_Decode(symbols_in,SF)
%   This code has been written by Bassel Al Homssi based on the LoRa
%   docding the PHY layer based on rpp0.
%   This code is to demodulate the LoRa signal to FSK signal
%   Code last updated 17.07.2019
symbols_hdr_pre = func4(func3(func1(mod(round(symbols_in(1:8)/4),2^(SF - 2))),(SF - 2),4),(SF - 2)) ;
symbols_hdr = func2(symbols_hdr_pre(1:5),4) ;
rdd_pld = floor(bitsra(symbols_hdr(2),5)) ;
symbols_pld_hdr = [(SF>7).*symbols_hdr_pre(end - SF + 8:end) func4(func3(func1(symbols_in(9:end)),SF,rdd_pld),length(func3(func1(symbols_in(9:end)),SF,rdd_pld)))] ;
symbols_out = [symbols_hdr func5(func2(func6(symbols_pld_hdr,rdd_pld,0),rdd_pld))] ;
end
function [symbols_out] = func1(symbols_in)
